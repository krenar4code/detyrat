// Marrja e dy numrave nga perdoruesi
let n1 = parseFloat(prompt("Shkruani numrin e pare:"));
let n2 = parseFloat(prompt("Shkruani numrin e dyte:"));

// Kontrolli dhe veprimi 
if (n1 > n2) {
    console.log("Shuma:", n1 + n2);
} else if (n2 > n1) {
    console.log("Prodhimi:", n1 * n2);
} else {
    console.log("Numrat jane te barabarte!");
}



